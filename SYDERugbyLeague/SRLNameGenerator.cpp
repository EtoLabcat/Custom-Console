string SRLNameGenerator::generateRandomOriginCountry()
{
	int number = rand() % countriesOfOrigin.size();
	return countriesOfOrigin[number];
}

string SRLNameGenerator::generateRandomName()
{
	return generateRandomFirstName() + " " + generateRandomLastName();
}

string SRLNameGenerator::generateRandomTeamName()
{
	string TeamName = generateRandomCity() + " " + generateRandomTeam();
	int attempts = 0;
	string FileName = "EngineFiles\\GameResults\\Teams\\" + TeamName + ".json";
	while (SYDEFileDefaults::exists(FileName.c_str()))
	{
		attempts++;
		string TeamName = generateRandomCity() + " " + generateRandomTeam();
		if (attempts >= 20)
		{
			return "Could Not Generate Error";
		}
	}
	return TeamName;
}

string SRLNameGenerator::generateRandomTeamNameSafe()
{
	return generateRandomCity() + " " + generateRandomTeam();
}

void SRLNameGenerator::generateAndAddToVector(vector<string>& vec)
{
	int number = rand() % BetSentence.size();
	vector<string> bets = BetSentence[number];
	for (int i = 0; i < bets.size(); i++)
	{
		vec.push_back(bets[i]);
	}
}

string SRLNameGenerator::generateRandomFirstName()
{
	int number = rand() % FirstNames.size();
	return FirstNames[number];
}

string SRLNameGenerator::generateRandomLastName()
{
	int number = rand() % LastNames.size();
	return LastNames[number];
}

string SRLNameGenerator::generateRandomCity()
{
	int number = rand() % CityNames.size();
	return CityNames[number];
}

string SRLNameGenerator::generateRandomTeam()
{
	int number = rand() % TeamNames.size();
	return TeamNames[number];
}
