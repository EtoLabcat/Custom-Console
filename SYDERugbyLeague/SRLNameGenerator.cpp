string SRLNameGenerator::generateRandomName()
{
	return generateRandomFirstName() + " " + generateRandomLastName();
}

string SRLNameGenerator::generateRandomTeamName()
{
	return generateRandomCity() + " " + generateRandomTeam();
}

string SRLNameGenerator::generateRandomFirstName()
{
	int number = rand() % FirstNames.size();
	return FirstNames[number];
}

string SRLNameGenerator::generateRandomLastName()
{
	int number = rand() % LastNames.size();
	return LastNames[number];
}

string SRLNameGenerator::generateRandomCity()
{
	int number = rand() % CityNames.size();
	return CityNames[number];
}

string SRLNameGenerator::generateRandomTeam()
{
	int number = rand() % TeamNames.size();
	return TeamNames[number];
}
